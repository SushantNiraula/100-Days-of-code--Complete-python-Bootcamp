## Let's see various mathematical operations in python 

# ## Addition
a = 10
b = 21
c = a + b
print(f'The addition of {a} and {b} is {c}') # The addition of 10 and 21 is 31
# ## Subtraction
d = b - a
print(f'The subtraction of {b} and {a} is {d}') # The subtraction of 21 and 10 is 11
# ## Multiplication
e = a * b
print(f'The multiplication of {a} and {b} is {e}') # The multiplication of 10 and 21 is 210
# ## Division
f = b / a
print(f'The division of {b} and {a} is {f}') # The division of 21 and 10 is 2.1
# ## Floor Division
g = b // a
print(f'The floor division of {b} and {a} is {g}') # The floor division of 21 and 10 is 2

